// Aufgabentyp-Helper für UI
export function getRandomAufgabe(modul, schwierigkeit) {
  const pool = pruefungsfragen[modul]?.[schwierigkeit] || [];
  if (pool.length === 0) return null;
  return pool[Math.floor(Math.random() * pool.length)];
}

